void getIchigoFont(char* font, uint8_t code) {
  //Serial.println(code,DEC);
  uint32_t adr = ichigoFont + (((uint32_t)code)<<3);
  if (code > 255)
    return;
  for (uint8_t i=0; i < 8; i++)
    font[i] = pgm_read_byte(adr+i);
}

